# Text Type = String
Name = "Dinesh"
print(Name)
print(type(Name))

#Numeric Type = Int , Float , Complex
x = 5
print(x)
print(type(x))

y = 20.5
print(y)
print(type(y))

z = 20j
print(z)
print(type(z))

#Sequence Types = list , tuples , range

fruits = ["apple","banana","cherry"]
print(fruits)

fruits_tuple = ("apple","banana","cherry")
print(fruits_tuple)

a = range(0, 10)
print(a)
print(list(a))

#Mapping types = Dictionaries

emp = {"Name":"Dinesh", "age": 27}
print(emp)

#Boolean Types = Bool
fix = bool(10)
print(fix)
print(type(fix))

#Set types = Set , Frozen set
fruits2 = {"apple","banana","cherry"}
print(fruits2)

fruits3 = frozenset({"apple","banana","cherry"})
print(fruits3)  